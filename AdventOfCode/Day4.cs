        public int Part1()
        {
            int total = 0;

            var mat = GetMatrix();
            var m = mat.GetLength(0);

            var xmas = new Regex("XMAS");
            var samx = new Regex("SAMX");

            for (int i = 0; i < m; i++)
            {
                var row = GetHoriz(mat, i);
                total += xmas.Count(row);
                total += samx.Count(row);
            }

            for (int j = 0; j < m; j++)
            {
                var row = GetVert(mat, j);
                total += xmas.Count(row);
                total += samx.Count(row);
            }

            foreach (var diag in mat.GetLeftToRightDiags())
            {
                total += xmas.Count(string.Concat(diag));
                total += samx.Count(string.Concat(diag));
            }

            foreach (var diag in mat.GetRightToLeftDiags())
            {
                total += xmas.Count(string.Concat(diag));
                total += samx.Count(string.Concat(diag));
            }

            return total;
        }

        public int Part2()
        {
            int total = 0;

            var mat = GetMatrix();
            var m = mat.GetLength(0);
            var n = mat.GetLength(1);

            for (int i = 0; i < m; i++)
                for (int j = 0; j < n; j++)
                {
                    if (i + 2 < m && j + 2 < n)
                    {
                        if (mat[i + 1, j + 1] == 'A')
                        {
                            if (((mat[i, j] == 'S' && mat[i + 2, j + 2] == 'M') || (mat[i, j] == 'M' && mat[i + 2, j + 2] == 'S')) &&
                                ((mat[i + 2, j] == 'S' && mat[i, j + 2] == 'M') || (mat[i + 2, j] == 'M' && mat[i, j + 2] == 'S')))
                            {
                                total++;
                            }
                        }
                    }

                }

            return total;
        }

        char[,] GetMatrix()
        {
            var n = input.IndexOf(Environment.NewLine);
            var m = input.Length / (n + 1);

            var mat = new char[m, n];

            for (int i = 0; i < m; i++)
            {
                for (int j = 0; j < n; j++)
                {
                    mat[i, j] = input[i * (n + Environment.NewLine.Length) + j];
                }
            }

            return mat;
        }

        static string GetHoriz(char[,] mat, int row)
        {
            var s = new StringBuilder();
            for (int j = 0; j < mat.GetLength(1); j++)
            {
                s.Append(mat[row, j]);
            }
            return s.ToString();
        }

        static string GetVert(char[,] mat, int col)
        {
            var s = new StringBuilder();
            for (int i = 0; i < mat.GetLength(0); i++)
            {
                s.Append(mat[i, col]);
            }
            return s.ToString();
        }

